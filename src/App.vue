<template>
  <div id="app">
    <router-view :key="this.$route.name + +this.$route.query._t"></router-view>
  </div>
</template>

<script>
export default {
  name: 'App',
  computed: {
    key () {
      return this.$route.name !== undefined ? this.$route.name + +this.$route.query._t : this.$route
    }
  }
}
</script>

